package models;

public class Distance {
	
	private double distance;
	private int id;
	
	
	public Distance() {
		super();
	}


	public Distance(double distance, int id) {
		super();
		this.distance = distance;
		this.id = id;
	}


	public double getDistance() {
		return distance;
	}


	public void setDistance(double distance) {
		this.distance = distance;
	}


	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}
	
	
	
	

}
